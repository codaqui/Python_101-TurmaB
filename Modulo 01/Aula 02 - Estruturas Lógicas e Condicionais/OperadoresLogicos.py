'''
Os operadores lógicos são usados para combinar expressões booleanas. Os principais operadores lógicos em Python são:
'''

'''
and: Retorna True se ambas as expressões forem verdadeiras.
'''

a = True
b = False
print(a and b)  # Saída: False

'''
or: Retorna True se pelo menos uma das expressões for verdadeira.
'''

a = True
b = False
print(a or b)  # Saída: True

'''
not: Inverte o valor booleano da expressão.
'''

a = True
print(not a)  # Saída: False